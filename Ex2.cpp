#include <iostream>     // Подключаем стандартную библиотеку ввода-вывода
#include <random>       // Подключаем библиотеку для генерации случайных чисел
#include <ctime>        // Подключаем библиотеку для работы с временем

using namespace std;    // Используем пространство имён std

int main() {
    const int n = 15;   // Задаём размер массива
    int arr[n];         // Объявляем массив из n элементов

    // Инициализация генератора случайных чисел с использованием minstd_rand
    minstd_rand generator(time(0));

    // Задаем диапазон случайных чисел от 150 до 300
    uniform_int_distribution<int> distribution(150, 300);

    // Заполняем массив случайными числами из указанного диапазона
    for (int i = 0; i < n; ++i) {
        arr[i] = distribution(generator);  // Генерируем случайное число и сохраняем его в массив
    }

    // Выводим содержимое массива на экран
    cout << "Сгенерированный массив:" << endl;
    for (int i = 0; i < n; ++i) {
        cout << arr[i] << " ";   // Выводим элемент массива с пробелом после каждого элемента
    }
    cout << endl;   // Переходим на новую строку для красивого вывода

    return 0;   // Возвращаем 0, что означает успешное завершение программы
}
